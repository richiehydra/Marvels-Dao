import sdk from "./1-initialize-sdk.js";

const editionDrop = sdk.getEditionDrop("0x51d479D93D413a006677f82e55e7abbd7Dd985D4");
const token = sdk.getToken("0x4F4d41ACcFe9845E81219785f2088275cA5d0cF1");

(async () => {
  try {
    const walletAddresses = await editionDrop.history.getAllClaimerAddresses(0);

    if (walletAddresses.length === 0) {
      console.log(
        "No NFTs have been claimed yet; maybe get some friends to claim your free NFTs!"
      );
      process.exit(0);
    }

    const airdropTargets = walletAddresses.map((address) => {
      const randomAmount = Math.floor(
        Math.random() * (10000 - 1000 + 1) + 1000
      );
      console.log("âœ… Going to airdrop", randomAmount, "tokens to", address);

      // Set up the target.
      const airdropTarget = {
        toAddress: address,
        amount: randomAmount,
      };

      return airdropTarget;
    });

    // Call transferBatch on all our airdrop targets.
    console.log("ðŸŒˆ Starting airdrop...");
    await token.transferBatch(airdropTargets);
    console.log(
      "âœ… Successfully airdropped tokens to all the holders of the NFT!"
    );
  } catch (err) {
    console.error("Failed to airdrop tokens", err);
  }
})();